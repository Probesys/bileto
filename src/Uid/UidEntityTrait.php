namespace App\Uid;

/**
 * @see UidEntityInterface
 */
trait UidEntityTrait
{
    public function getUid(): ?string
    {
        return $this->uid;
    }

    public function setUid(string $uid): self
    {
        $this->uid = $uid;

        return $this;
    }
}
